# git 分布式版本控制系统
# 本地仓库，远程仓库，gitee，github
# 离线可提交，无法做详细权限管理

# 集中版本控制系统（svn）
# 目录级别的权限管理
# 离线就不能提交 svn盘损坏，数据消失

# git bash
# git init # 初始化一个空的仓库
# rm -rf .git  删除仓库
# git status   查看状态
# git add file.txt  追踪file.txt
# git commit -m " add file.txt"  提交所追踪的文件，更新历史版本

# git log 查看历史记录
# git log -p  查看历史提交详细
# git add -all 提交所有

# git reset --hard 回滚到上一个版本 --soft回滚版本内容不变
